package basic.begin;
/**
 * 문서 생성용 주석입니다.
 * 배포용 문서에 설명을 달아 놓을 때 사용합니다.
 * @author EG
 * @version 1.0
 * @since 2023.02.17 ~ 
 */
// 이 파일은 주석을 연습하는 파일입니다.
public class CommentsExample {

	public static void main(String[] args) {

		/*
		# main 함수(메서드)는 프로그램 실행 시 자동으로 동작하는 함수이며, 
		반드시 필요한 함수입니다. 
		
		# 자바 코드를 해석하여 실행시키는 자바 가상 머신(JVM)은 
		프로그램 실행 시 자동으로 main 함수를 불러서 내부의 코드를 
		실행시킵니다.
		*/
		
		// ★ Ctrl + S : 저장 단축키 ★
		// syso(sysout) + Ctrl + space : 빠른 출력 함수를 생성.
		// Ctrl + F11 : 코드 실행 단축키
		// 원하는 위치에 커서 이동 혹은 드래그 후 Ctrl + / : 빠른 주석 처리, 해제
		// 원하는 위치에 커서 이동 후 Ctrl + Alt + 방향키 : 코드 복사
		// Ctrl + Z : 실행 취소
		// 원하는 위치 드래그 Ctrl + Shift + / : 문장 주석 (줄바꿈으로 인해 비추천)
		// /** ~ */ : 문서를 설명할 때 쓰는 주석
		
		System.out.println("안녕하세요~");
		System.out.println("반갑습니다~");
	}

}
